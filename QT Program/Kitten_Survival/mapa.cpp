void Mapa::CargarMapa()
{
    GenerarMapa();
    for (int i = 0;i<bloques_y_mapa;i++)
    {
        for (int j = 0;j<bloques_x_mapa;j++)
        {
            if (Matriz[i][j] == 0)
            {
                bl_mapa[i][j] = new Bloques(0);
            }
            else if (Matriz[i][j] == 1)
            {
                bl_mapa[i][j] = new Bloques(1);
            }
            addItem(bl_mapa[i][j]);
            bl_mapa[i][j]->setPos(j*bloques_ancho,i*bloques_alto);
        }
    }

}
